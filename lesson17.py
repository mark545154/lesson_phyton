def vacation(emp_list):
    vac_list = []
    for person in emp_list:
        person[2] = datetime.strptime(person[2], '%d.%m.%Y')
        experience = (datetime.today() - person[2]).days / 365.25
        if experience >= 0.5:
            vac_list.append(person)
    return vac_list


