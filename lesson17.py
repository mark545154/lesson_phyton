def gender_holiday(emp_list, bonus):
    for person in emp_list:
        if person[-1].lower() == 'жен' and datetime.now().month == 3 or \
                person[-1].lower() == 'муж' and datetime.now().month == 2:
            person[3] += bonus

    return emp_list


